SELECT
    Y.CART_ID
FROM
(SELECT DISTINCT
    CART_ID,
    NAME
FROM
    CART_PRODUCTS
WHERE
    NAME = "Yogurt") Y
LEFT JOIN
(SELECT DISTINCT
    CART_ID,
    NAME
FROM
    CART_PRODUCTS
WHERE
    NAME = "Milk") M
ON
    Y.CART_ID = M.CART_ID
WHERE
    M.NAME IS NOT NULL
ORDER BY
    Y.CART_ID
